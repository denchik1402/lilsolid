        random.shuffle(generic_rev)

        def pick_review_for_product(product, dr, tr, gr):
            """Подбирает отзыв по типу товара. Удаляет из списка (без повторений)."""
            ptype = get_product_type(product)
            if ptype == "device" and dr:
                return dr.pop(), dr, tr, gr
            if ptype == "terea" and tr:
                return tr.pop(), dr, tr, gr
            if gr:
                return gr.pop(), dr, tr, gr
            if dr:
                return dr.pop(), dr, tr, gr
            if tr:
                return tr.pop(), dr, tr, gr
            return None, dr, tr, gr

        def pick_reviews(target_count):
            result = []
            dr, tr, gr = list(device_rev), list(terea_rev), list(generic_rev)
            for _ in range(target_count):
                product = random.choice(products)
                review, dr, tr, gr = pick_review_for_product(product, dr, tr, gr)
                if review is None:
                    break
                result.append((review, product))
            return result

        to_add = 100 - existing
        pairs = pick_reviews(to_add)

        base_date = datetime.now(timezone.utc) - timedelta(days=90)

        for (name, rating, text, _), product in pairs:
            days_ago = random.randint(0, 90)
            created = base_date + timedelta(days=days_ago)
            review = Review(
                product_id=product.id,
                customer_name=name,
                rating=rating,
                text=text,
                status='approved',
                created_at=created
            )
            db.session.add(review)

        db.session.commit()
        print(f"Добавлено {len(pairs)} отзывов. Всего отзывов: {Review.query.count()}")
        print(f"  Устройства: {len(device_products)}, TEREA: {len(terea_products)}, прочие: {len(generic_products)}")


