def get_product_type(product):
    """Определяет тип товара: device (устройство) или terea (стики)."""
    name_lower = (product.name or "").lower()
    cat_name = (product.category.name if product.category else "").lower()
    # TEREA, Terea — стики
    if "terea" in name_lower or "стики" in name_lower or "стик" in name_lower or "terea" in cat_name:
        return "terea"
    # IQOS, ILUMA — устройства (держатели, девайсы)
    if "iqos" in name_lower or "iluma" in name_lower or "устройство" in name_lower or "держатель" in name_lower:
        return "device"
    return "generic"


